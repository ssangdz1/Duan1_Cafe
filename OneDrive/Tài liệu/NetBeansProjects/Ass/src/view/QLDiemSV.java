    private void loadDataToTable() {
        list.clear();
        model.setRowCount(0);
        list = dsv.getAllsDiem();
        for (Diem x : list) {
            
            model.addRow(new Object[]{x.getMasv(), x.getTensv(),
                x.getTa(), x.getTh(),
                x.getGdtc(), x.getTongKet()});
        }
        model.setRowCount(3);
    }

    public void showdetail() {
        Diem x = list.get(index);
        txt_ma.setText(x.getMasv());
        txt_ten.setText(x.getTensv());
        txt_ta.setText(String.valueOf(x.getTa()));
        txt_th.setText(String.valueOf(x.getTh()));
        txt_tc.setText(String.valueOf(x.getGdtc()));
        double d = (double)Math.round(x.getTongKet()*100)/100;
        txt_tk.setText(String.valueOf(d));
    }
    
    public Diem readForm(){
        
        
        Diem x = new Diem();
        x.setMasv(txt_ma.getText());
        x.setTensv(txt_ten.getText());
        x.setTa(Double.valueOf(txt_ta.getText()));
        x.setTh(Double.valueOf(txt_th.getText()));
        x.setGdtc(Double.valueOf(txt_tc.getText()));
        return x ;
    }
    
    
    public boolean validateForm(){
        if (txt_ma.getText().isEmpty()) {
            JOptionPane.showMessageDialog(this, "Mã không được để trống");
            return false;
        }
        
        if(txt_ta.getText().isEmpty()){
            JOptionPane.showMessageDialog(this, "Điểm tiếng anh không được để trống");
            txt_ta.requestFocus();
            return false;
        }
        if(txt_th.getText().isEmpty()){
            JOptionPane.showMessageDialog(this, "Điểm tin học không được để trống");
            txt_th.requestFocus();
            return false;
        }
        if(txt_tc.getText().isEmpty()){
            JOptionPane.showMessageDialog(this, "Điểm GDTC không được để trống");
            txt_tc.requestFocus();
            return false;
        }
        if(!txt_ta.getText().matches("\\d+(\\.\\d+)?")){
             JOptionPane.showMessageDialog(this, "Điểm tiếng anh sai kiểu dữ liệu");
            txt_ta.requestFocus();
            return false;
        }
        if(!txt_th.getText().matches("\\d+(\\.\\d+)?")){
             JOptionPane.showMessageDialog(this, "Điểm tiếng anh sai kiểu dữ liệu");
            txt_th.requestFocus();
            return false;
        }
        if(!txt_tc.getText().matches("\\d+(\\.\\d+)?")){
             JOptionPane.showMessageDialog(this, "Điểm tiếng anh sai kiểu dữ liệu");
            txt_tc.requestFocus();
            return false;
        }
        if(Double.parseDouble(txt_ta.getText())>10){
             JOptionPane.showMessageDialog(this, "Điểm tiếng anh phải nhỏ hơn 10");
            txt_ta.requestFocus();
            return false;
        }
        if(Double.parseDouble(txt_th.getText())>10){
             JOptionPane.showMessageDialog(this, "Điểm tin học phải nhỏ hơn 10");
            txt_th.requestFocus();
            return false;
        }
        if(Double.parseDouble(txt_tc.getText())>10){
             JOptionPane.showMessageDialog(this, "Điểm thể chất phải nhỏ hơn 10");
            txt_tc.requestFocus();
            return false;
        }
        return true;
    }
