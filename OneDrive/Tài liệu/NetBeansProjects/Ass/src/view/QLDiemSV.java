    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:
        String ma = txt_search.getText();
        if(ma==null){
            JOptionPane.showMessageDialog(this,"vui lòng nhập mã muốn tìm kiếm");
            return;
        }
        Diem x = new Diem();
        for (Diem n : list) {
            if (n.getMasv().equalsIgnoreCase(ma)) {
                x = n;
                break;
            }
        }
        if(x==null){
            JOptionPane.showMessageDialog(this,"không tìm thấy ma sv phù hợp");
            return;
        }
        model.setRowCount(0);
        model.addRow(new Object[]{x.getMasv(), x.getTensv(),
            x.getTa(), x.getTh(), x.getGdtc(), x.getTongKet()});
    }//GEN-LAST:event_jButton1ActionPerformed

