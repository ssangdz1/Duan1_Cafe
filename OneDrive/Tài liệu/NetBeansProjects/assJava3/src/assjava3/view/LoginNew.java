    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:

        try {
            Connection con = DBContext.getConnection();
            String sql = "Select * from Users where userName = ? and pass = ?";
            PreparedStatement stm = con.prepareStatement(sql);
            stm.setString(1, txtUsername.getText());
            stm.setString(2, txtPassword.getText());
            ResultSet rs = stm.executeQuery();
            if (checkNull()) {
                if (rs.next()) {
                    if (rs.getString("role").equalsIgnoreCase("admin")) {
                        JOptionPane.showMessageDialog(this, "Đăng nhập thành công với vai trò " + "'Admin'");
                        MenuQLSV mn4 = new MenuQLSV();
                        mn4.setVisible(true);
                        mn4.setLocationRelativeTo(null);
                        this.dispose();
                        saveToFile(txtUsername.getText(), txtPassword.getText(), "admin");
                    }
                    if (rs.getString("role").equalsIgnoreCase("user")) {
                        JOptionPane.showMessageDialog(this, "Đăng nhập thành công với vai trò " + "'Giảng viên'");
                        MenuQLD mn3 = new MenuQLD();
                        mn3.setVisible(true);
                        mn3.setLocationRelativeTo(null);
                        this.dispose();
                        saveToFile(txtUsername.getText(), txtPassword.getText(), "user");
                    }

                } else {
                    JOptionPane.showMessageDialog(this, "Sai tài khoản hoặc mật khẩu!");
                }
            }

        } catch (Exception e) {
            e.printStackTrace();
        }
    }//GEN-LAST:event_jButton1ActionPerformed

