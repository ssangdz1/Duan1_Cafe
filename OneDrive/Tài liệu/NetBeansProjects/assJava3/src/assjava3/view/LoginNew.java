    public boolean checkNull() {
        if (txtUsername.getText().isEmpty()) {
            lblLoiUser.setText("Vui lòng nhập tài khoản!");
            txtUsername.requestFocus();
            return false;
        } else {
            lblLoiUser.setText(""); // Xóa thông báo lỗi về tài khoản khi người dùng điền đúng thông tin
        }
        if (txtPassword.getPassword().length == 0) {
            lblLoipass.setText("Vui lòng nhập mật khẩu!");
            txtPassword.requestFocus();
            return false;
        } else {
            lblLoipass.setText(""); // Xóa thông báo lỗi về mật khẩu khi người dùng điền đúng thông tin
        }
        return true;
    }
