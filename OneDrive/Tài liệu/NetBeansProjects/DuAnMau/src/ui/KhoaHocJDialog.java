    private void clearForm() {
        this.setForm(new KhoaHoc());
        this.updateStatus();
        this.row = -1;
        updateStatus();
    }

    private void delete() {
        KhoaHoc x = dao.selectAll().get(row);
        int chon = JOptionPane.showConfirmDialog(this, "ban chac chan muon xoa chu");
        if(chon!=0) return;
        try {
            dao.delete(x.getMaKH());
            this.fillTable();
            this.clearForm();
            row=-1;
            MsgBox.alert(this, "Xóa Thành Công");
            
        } catch (Exception e) {
            MsgBox.alert(this, "Xóa Thất Bại");
        }
    }

    private void update() {
        KhoaHoc model = getForm();
         model.setMaKH(Integer.valueOf(tblkhoahoc.getValueAt(row, 0)+""));
        try {
            dao.update(model);
            this.fillTable();
            this.clearForm();
            MsgBox.alert(this, "Sửa Thành Công");
        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, e);
            MsgBox.alert(this, "Sửa Thất Bại");
        }
    }

    private void insert() {
        KhoaHoc model = getForm();
        model.setMaNV(Auth.user.getMaNV());
        model.setNgayTao(XDate.now());
        try {
            dao.insert(model);
            this.fillTable();
            this.clearForm();
            MsgBox.alert(this, "Thêm Thành Công");
        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, e);
            MsgBox.alert(this, "Thêm Thất Bại");
        }
    }

    private KhoaHoc getForm() {
        ChuyenDe x = cddao.selectByTenCD(txttencd.getText());
        String patten = "yyyy-MM-dd";
        KhoaHoc model = new KhoaHoc();
        model.setNgayKG(XDate.toDate(txtkhaigiang.getText(), patten));
        model.setHocPhi(Double.valueOf(txthocphi.getText()));
        model.setThoiLuong(Integer.parseInt(txtthoiluong.getText()));
        model.setMaNV(txtnguoitao.getText());
        model.setNgayTao(XDate.toDate(txtngaytao.getText(), patten));
        model.setGhiChu(txtghichu.getText());
        model.setMaCD(x.getMaCD());
        return model;
    }
