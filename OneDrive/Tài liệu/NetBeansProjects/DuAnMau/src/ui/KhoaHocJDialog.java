    void fillComboBoxChuyenDe() {
        DefaultComboBoxModel model = (DefaultComboBoxModel) cbochuyende.getModel();
        model.removeAllElements();
        List<ChuyenDe> list = cddao.selectAll();
        for (ChuyenDe cd : list) {
            model.addElement(cd);
        }
    }

    void fillTable() {
        DefaultTableModel model = (DefaultTableModel) tblkhoahoc.getModel();
        model.setRowCount(0);
        try {
            List<KhoaHoc> list = dao.selectAll();
            for (KhoaHoc khoaHoc : list) {
                model.addRow(new Object[]{
                    khoaHoc.getMaKH(),
                    khoaHoc.getThoiLuong(),
                    khoaHoc.getHocPhi(),
                    khoaHoc.getNgayKG(),
                    khoaHoc.getMaNV(),
                    khoaHoc.getNgayTao()
                });
            }
        } catch (Exception e) {
            MsgBox.alert(this, "lỗi câu truy vấn1");
        }
    }

    void edit() {
        try {
            int makh = Integer.valueOf(tblkhoahoc.getValueAt(row, 0) + "");
            KhoaHoc model = dao.selectById(makh);

            if (model != null) {
                setForm(model);
                updateStatus();
                tabs.setSelectedIndex(0);
            }
        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, e);
            MsgBox.alert(this, "Lỗi câu truy vấn");
        }
    }

    void setForm(KhoaHoc model) {
        ChuyenDe mol = new ChuyenDe();
        if (model != null) {
            mol = cddao.selectById(model.getMaCD());
//            txttencd.setText(mol.getTenCD());
        } else {
            txttencd.setText("");
        }

        txtkhaigiang.setText(model.getNgayKG() + "");
        txthocphi.setText(String.valueOf(model.getHocPhi()));
        txtthoiluong.setText(String.valueOf(model.getThoiLuong()));
        txtnguoitao.setText(String.valueOf(model.getMaNV()));
        txtngaytao.setText(model.getNgayTao() + "");
    }

    void updateStatus() {
        boolean edit = (row >= 0);
        boolean first = (this.row == 0);
        boolean last = (this.row == tblkhoahoc.getRowCount() - 1);
        //trang thai form
        btnThem.setEnabled(!edit);
        BtnSua.setEnabled(edit);
        btnXoa.setEnabled(edit);
        //trang thai dieu huong
        btnFirts.setEnabled(edit && !first);
        btnPrew.setEnabled(edit && !first);
        btnNext.setEnabled(edit && !last);
        btnLast.setEnabled(edit && !last);
    }

