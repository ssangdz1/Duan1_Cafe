    private void filltalehocvien() {
        DefaultTableModel hvModel = (DefaultTableModel) tblhocvien.getModel();
        hvModel.setRowCount(0);
        KhoaHoc x = (KhoaHoc) cbokhoahoc.getSelectedItem();
        if (x != null) {
            List<HocVien> list = hvdao.selectByKhoaHoc(x.getMaKH());
            for (HocVien k : list) {
                hvModel.addRow(new Object[]{k.getMaHV(), k.getMaKH(),
                    k.getMaNH(), k.getDiem()});
            }
        }
        filltablenguoihoc();
    }

    private void filltablenguoihoc() {
        DefaultTableModel nhmodel = (DefaultTableModel) tblnguoihoc.getModel();
        nhmodel.setRowCount(0);
        KhoaHoc x = (KhoaHoc) cbokhoahoc.getSelectedItem();
        if (x != null) {
            List<NguoiHoc> list = nhdao.selectNotInCourse(x.getMaKH(), "");
            for (NguoiHoc k : list) {
                nhmodel.addRow(new Object[]{k.getMaNH(), k.getHoTen(),
                    k.getNgaySinh(), k.isGioiTinh() ? "Nam" : "Nữ"});
            }
        }
    }

    private void addHocVien() {
        KhoaHoc x = (KhoaHoc)cbokhoahoc.getSelectedItem();
        int[] rows = tblnguoihoc.getSelectedRows();
        for(int row : rows){
            String manh = tblnguoihoc.getValueAt(row, 0)+"";
            HocVien hv = new HocVien();
            hv.setMaKH(x.getMaKH());
            hv.setMaNH(manh);
            hv.setDiem(0);
            hvdao.insert(hv);
        }
        filltalehocvien();
    }

    private void xoaHocVien(int ma) {
        int chon = JOptionPane.showConfirmDialog(null, "Bạn chắc chắn muốn xóa chứ");
        if(chon!=0){
            return;
        }
        try {
            hvdao.delete(ma);
            filltalehocvien();
            MsgBox.alert(this, "Xóa Thành Công");
        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, e);
            MsgBox.alert(this, "Xóa Thất Bại");
        }
    }

    private void capNhatDiem() {
        int row = tblhocvien.getRowCount();
        for(int i=0;i<row;i++){
            int mahv = (Integer)tblhocvien.getValueAt(i, 0);
            String diemsString = tblhocvien.getValueAt(i, 3)+"";
            if (!diemsString.matches("\\d+(\\.\\d+)?")) {
                JOptionPane.showMessageDialog(this, "Sai định dạng");
                return;
            }else if(diemsString.equals("")){
                JOptionPane.showMessageDialog(this, "Không được để trống");
                return;
            }else if(Double.valueOf(diemsString)>100){
                JOptionPane.showMessageDialog(this, "điểm không được quá 100");
                return;
            }
            
            double diem = Double.valueOf(diemsString);
            HocVien x = hvdao.selectById(mahv);
            x.setDiem(diem);
            hvdao.update(x);
            System.out.println(row);
            System.out.println(diem);
        }
        filltalehocvien();
        MsgBox.alert(null, "Cập nhật điểm thành công");
    }
    
