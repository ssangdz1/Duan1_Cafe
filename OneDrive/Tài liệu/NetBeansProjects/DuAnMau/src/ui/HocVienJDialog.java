    void fillComboboxChuyenDe() {
        DefaultComboBoxModel model = (DefaultComboBoxModel) cbochuyende.getModel();
        model.removeAllElements();
        try {
            List<ChuyenDe> list = cddao.selectAll();
            for (ChuyenDe cd : list) {
                model.addElement(cd);
            }
            fillComboboxKhoaHoc();
        } catch (Exception e) {
            e.printStackTrace();
            MsgBox.alert(this, "lỗi câu truy vấn!");
        }
    }

    void fillComboboxKhoaHoc() {
        DefaultComboBoxModel model = (DefaultComboBoxModel) cbokhoahoc.getModel();
        model.removeAllElements();
        try {
            ChuyenDe chyenDe = (ChuyenDe) cbochuyende.getSelectedItem();
            if (chyenDe != null) {
                List<KhoaHoc> list = khdao.selectByChuyenDe(chyenDe.getMaCD());
                if (list != null || !list.isEmpty()) {
                    for (KhoaHoc kh : list) {
                        model.addElement(kh);
                    }
                } else {
                    JOptionPane.showMessageDialog(null, "không có khóa học nào");
                    return;
                }
                filltalehocvien();
            }
        } catch (Exception e) {
            e.printStackTrace();
            MsgBox.alert(this, "lỗi câu truy vấn!");
        }
    }

