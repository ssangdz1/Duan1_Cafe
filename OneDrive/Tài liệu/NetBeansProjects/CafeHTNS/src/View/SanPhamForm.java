    private void loadTable() {
        mol.setRowCount(0);
        ArrayList<SanPham> list = sps.getAllSP();
        for (SanPham sp : list) {
            mol.addRow(new Object[]{
                sp.getMaSP(),
                sp.getTenSP(),
                sp.getLoai(),
                sp.getSoLuong(),
                sp.getGia(),
                sp.getHinh(),});
        }
    }

    private void showDetail() {
        int index = tblBang.getSelectedRow();
        txtMa.setText(tblBang.getValueAt(index, 0).toString());
        txtTen.setText(tblBang.getValueAt(index, 1).toString());
        txtSoLuong.setText(tblBang.getValueAt(index, 3).toString());
        txtGia.setText(tblBang.getValueAt(index, 4).toString());
        cboLoai.setSelectedItem(tblBang.getValueAt(index, 2));
        updateHinh(tblBang.getValueAt(index, 5).toString());
    }

    public void updateHinh(String images) {
        ImageIcon icon1 = new ImageIcon("src/images/" + images);
        Image im = icon1.getImage();
        ImageIcon icon = new ImageIcon(im.getScaledInstance(lblAnh3.getWidth(), lblAnh3.getHeight(), im.SCALE_SMOOTH));
        lblAnh3.setIcon(icon);
    }

    public SanPham getInputForm() {
    SanPham sp = new SanPham();
    sp.setMaSP(txtMa.getText());
    sp.setTenSP(txtTen.getText());
    
    // Sử dụng selectedItem để lấy giá trị thực sự từ ComboBox
    sp.setLoai((String) cboLoai.getSelectedItem()); 
    
    try {
        // Kiểm tra giá trị có hợp lệ hay không trước khi chuyển đổi
        sp.setSoLuong(Integer.parseInt(txtSoLuong.getText()));
        sp.setGia(Double.parseDouble(txtGia.getText()));
    } catch (NumberFormatException e) {
        // Xử lý lỗi khi người dùng nhập không đúng định dạng số
        e.printStackTrace();
        // Hoặc hiển thị một thông báo lỗi cho người dùng
    }

    // Kiểm tra và xử lý giá trị của strHinh
    sp.setHinh((strHinh != null) ? strHinh : "");

    return sp;
}
    public boolean check() {
        if (txtMa.getText().trim().isEmpty()) {
            JOptionPane.showMessageDialog(this, "Mã sản phẩm không được để trống");
            txtMa.requestFocus();
            return false;
        }
        List<SanPham> allSP = sps.getAllSP();
        for (SanPham d : allSP) {
            if (txtMa.getText().trim().equalsIgnoreCase(d.getMaSP())) {
                // Mã sinh viên đã tồn tại
                JOptionPane.showMessageDialog(this, "Mã sản phẩm đã tồn tại, vui lòng kiểm tra lại!");
                txtMa.requestFocus();
                return false; // Không cho phép thêm mới
            }
        }
        if (txtTen.getText().trim().isEmpty()) {
            JOptionPane.showMessageDialog(this, "Tên sản phẩm không được để trống");
            txtTen.requestFocus();
            return false;
        }

        if (txtGia.getText().trim().isEmpty()) {
            JOptionPane.showMessageDialog(this, "Giá sản phẩm không được để trống");
            txtGia.requestFocus();
            return false;
        }
        try {
            double gia = Double.parseDouble(txtGia.getText());
            if (gia == 0 || gia < 0) {
                JOptionPane.showMessageDialog(this, "Giá sản phẩm phải lớn hơn 0!");
                txtGia.requestFocus();
                return false;
            }
        } catch (NumberFormatException e) {
            JOptionPane.showMessageDialog(this, "Giá phải là một số nguyên hợp lệ");
            txtGia.requestFocus();
            return false;
        }

        if (txtSoLuong.getText().trim().isEmpty()) {
            JOptionPane.showMessageDialog(this, "Số lượng sản phẩm không được để trống");
            txtSoLuong.requestFocus();
            return false;
        }
        try {
            double soluong = Integer.parseInt(txtSoLuong.getText());
            if (soluong == 0 || soluong < 0) {
                JOptionPane.showMessageDialog(this, "Số lượng sản phẩm phải lớn hơn 0!");
                txtSoLuong.requestFocus();
                return false;
            }
        } catch (NumberFormatException e) {
            JOptionPane.showMessageDialog(this, "Số lượng phải là một số nguyên hợp lệ");
            txtSoLuong.requestFocus();
            return false;
        }
        return true;
    }

