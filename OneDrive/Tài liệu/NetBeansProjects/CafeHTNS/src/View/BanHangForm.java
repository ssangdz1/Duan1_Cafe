    public boolean gioiHanSoLuong(int gioihan) {
        if (gioihan < 0 || gioihan == 0){
            JOptionPane.showMessageDialog(this, "Vui lòng tăng số lượng đặt hàng!");
            return false;
        }
        if(gioihan>=10){
            JOptionPane.showMessageDialog(this, "Vui lòng giảm số lượng đặt hàng!");
            return false;
        }
        return true;
    }
    
