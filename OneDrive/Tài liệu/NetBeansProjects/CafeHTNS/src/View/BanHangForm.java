    private void btnInActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnInActionPerformed
        String path = "";
        arr = hdcts.timTheoMAHD2(txtMaHd.getText());
        JFileChooser j = new JFileChooser("\\Users\\sang\\OneDrive\\Tài liệu\\NetBeansProjects\\CafeHTNS\\Bill");
        j.setFileSelectionMode(JFileChooser.DIRECTORIES_ONLY);
        int x = j.showSaveDialog(this);
        if (x == JFileChooser.APPROVE_OPTION) {
            path = j.getSelectedFile().getPath();
        }
        Document doc = new Document();
        try {
            String fileName = "\\Bill" + stt + ".pdf";
            PdfWriter.getInstance(doc, new FileOutputStream(path + fileName));
            doc.open();
            Paragraph ngayXuatHDPara = new Paragraph("************************************************Cafe HTNS***********************************************\n"
                    + "Gio : " + txtTime.getText() + "\nNgay : " + txtDay.getText() + "\n"
                    + "***********************************************************************************************************\n");
            doc.add(ngayXuatHDPara);
            Paragraph maHoaDonPara = new Paragraph("Ma hoa don: " + txtMaHd.getText() + "\n");
            doc.add(maHoaDonPara);
            Paragraph khoangTrong = new Paragraph("                                                     ");
            doc.add(khoangTrong);
            PdfPTable tb = new PdfPTable(3);
            tb.addCell("Ten san pham");
            tb.addCell("So luong");
            tb.addCell("Gia");
            double tongTien = 0;
            // Thêm bảng vào tài liệu
            for (HoaDon hd : arr) {
                String cot3 = hd.getTenSp();
                String cot4 = hd.getSoluong() + "";
                String cot5 = hd.getGia() + "";
                tb.addCell(cot3);
                tb.addCell(cot4);
                tb.addCell(cot5);
                tongTien = hd.getTongtien();
            }
            doc.add(tb);
            Paragraph tongTienPara = new Paragraph("\n************************************************************************************************************\n"
                    + "                                                             Tong cong: \t\t\t" + String.format("%.0f", tongTien) + " VND" + "\n"
                    + "*********************************************Cam on quy khach*******************************************\n");
            // Đặt chữ hoa và màu xanh
            tongTienPara.setFont(FontFactory.getFont(FontFactory.HELVETICA_BOLD, 20, Font.BOLDITALIC));
            doc.add(tongTienPara);
            JOptionPane.showMessageDialog(this, "Xuất PDF thành công");
            stt++;
            clearForm();
        } catch (Exception e) {
            e.printStackTrace();
            JOptionPane.showMessageDialog(this, "Lỗi xuất PDF: " + e.getMessage());
        }
        doc.close();
    }//GEN-LAST:event_btnInActionPerformed

