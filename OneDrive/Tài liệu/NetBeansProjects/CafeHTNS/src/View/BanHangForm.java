    private double getGiaFromLine(String line) {
        String[] parts = line.split("\t");
        if (parts.length >= 3) {
            try {
                return Double.parseDouble(parts[2].trim());
            } catch (NumberFormatException e) {
                e.printStackTrace();
            }
        }
        return 0.0;
    }

    private void removeLinesAndUpdateTotal(String tenSanPham, int soLuong) {
        String[] lines = txtHoaDon.getText().split("\n");
        StringBuilder updatedText = new StringBuilder();
        // Duyệt ngược từ cuối danh sách dòng
        for (int i = lines.length - 1; i >= 0; i--) {
            // Kiểm tra xem dòng có chứa thông tin sản phẩm cần xóa không
            if (lines[i].contains(tenSanPham) && lines[i].contains(String.valueOf(soLuong))) {
                // Nếu có, giảm giá sản phẩm đã chọn khỏi total
                total -= getGiaFromLine(lines[i]);
            } else {
                // Nếu không, giữ lại dòng đó
                updatedText.insert(0, lines[i] + "\n");
            }
        }
        // Cập nhật văn bản với các dòng đã được giữ lại
        txtHoaDon.setText(updatedText.toString());
        // Cập nhật lại giá tổng
        dudate();
    }

    public void updateHinh(String images, JLabel lblAnh) {
        // Đường dẫn tới thư mục chứa ảnh (thay đổi theo cấu trúc dự án của bạn)
        String imagePath = "src/images/" + images;
        // Tạo ImageIcon từ đường dẫn ảnh
        ImageIcon originalIcon = new ImageIcon(imagePath);
        // Scale ảnh một lần và sử dụng cho tất cả các JLabel
        ImageIcon scaledIcon = scaleImage(originalIcon, lblAnh.getWidth(), lblAnh.getHeight());
        // Đặt icon cho JLabel
        lblAnh.setIcon(scaledIcon);
    }

